int	ft_atoi(char *str)
{
	int	i;
	int	sum;
	int	k;

	k = 1;
	i = 0;
	sum = 0;
	while (str[i] != '\0' && ((str[i] >= '0' && str[i] <= '9') ||
				(i == 0 && (str[0] == '-' || str[0] == '+'))))
	{
		if (str[i] == '-' || str[i] == '+')
		{
			i++;
			continue;
		}
		sum = (sum * k) + (str[i] - '0');
		k = 10;
		++i;
	}
	if (str[0] == '-')
		sum *= -1;
	return (sum);
}
