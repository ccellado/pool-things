int	ft_max(int *tab, int length)
{
	int i;
	int max;

	if (length == 0)
		return (0);
	i = 1;
	max = tab[0];
	while (i < length)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
